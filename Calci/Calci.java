public class Calci
{
    public static void main(String args[])
    {
        int a=9, b=3;
        int sum=a+b;
        int sub=a-b;
        int mul=a*b;
        float div=a/b;
        float mod=a%b;
        System.out.println("Sum = " + sum);
        System.out.println("Difference = " + sub);
        System.out.println("Product = "+ mul);
        System.out.println("Division = " + div);
        System.out.println("Modulus = " + mod);
    }
}